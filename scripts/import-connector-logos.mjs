/**
 * Import real brand logos for Agentis connectors from a local n8n checkout.
 *
 * n8n ships an MIT-friendly library of brand SVGs (one per integration node).
 * We copy the ones matching our connector catalog into apps/web/public/connectors/
 * so the integration UI shows each company's real logo instead of a generic glyph.
 *
 * Usage: node scripts/import-connector-logos.mjs [path-to-n8n]
 */
import { readdirSync, statSync, mkdirSync, copyFileSync, existsSync, readFileSync } from 'node:fs';
import { join, basename, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const repo = join(dirname(fileURLToPath(import.meta.url)), '..');
const n8nRoot = process.argv[2] || process.env.N8N_ROOT;
if (!n8nRoot) {
  console.error('Usage: node scripts/import-connector-logos.mjs <path-to-n8n-checkout>  (or set N8N_ROOT)');
  process.exit(1);
}
const nodesBase = join(n8nRoot, 'packages/nodes-base');
const nodesDir = join(nodesBase, 'nodes');
const langchainDir = join(n8nRoot, 'packages/@n8n/nodes-langchain/nodes');
const outDir = join(repo, 'apps/web/public/connectors');

// Connector ids come straight from packages/integrations/src/manifests.ts.
const manifestsSrc = readFileSync(join(repo, 'packages/integrations/src/manifests.ts'), 'utf8');
const connectorIds = [...manifestsSrc.matchAll(/manifest\('([a-z0-9_]+)'/g)].map((m) => m[1]);

// Connectors with no brand (generic protocol/core nodes) — skip, keep glyph fallback.
const NO_BRAND = new Set(['http_request', 'webhook_send', 'email_smtp', 'ftp', 'rss_feed', 'sqs', 'pubsub', 's3']);

// Explicit map for compound paths the basename matcher can't resolve.
// Paths are relative to packages/nodes-base (covers both nodes/ and credentials/icons/).
const OVERRIDE = {
  gmail: 'nodes/Google/Gmail/gmail.svg',
  google_sheets: 'nodes/Google/Sheet/googleSheets.svg',
  google_docs: 'nodes/Google/Docs/googleDocs.svg',
  google_drive: 'nodes/Google/Drive/googleDrive.svg',
  google_calendar: 'nodes/Google/Calendar/googleCalendar.svg',
  google_analytics: 'nodes/Google/Analytics/analytics.svg',
  outlook: 'nodes/Microsoft/Outlook/outlook.svg',
  elasticsearch: 'nodes/Elastic/Elasticsearch/elasticsearch.svg',
  twitter_x: 'nodes/Twitter/x.svg',
  s3: 'nodes/Aws/S3/s3.svg',
  sqs: 'nodes/Aws/SQS/sqs.svg',
  sentry: 'nodes/SentryIo/sentryio.svg',
  auth0: 'credentials/icons/Auth0.svg',
  datadog: 'credentials/icons/Datadog.svg',
};

function walk(dir, acc) {
  if (!existsSync(dir)) return acc;
  for (const e of readdirSync(dir)) {
    const p = join(dir, e);
    const st = statSync(p);
    if (st.isDirectory()) walk(p, acc);
    else if (/\.svg$/i.test(e) && !/\.dark\.svg$/i.test(e)) acc.push(p);
  }
  return acc;
}

const allSvgs = [...walk(nodesDir, []), ...walk(langchainDir, [])];
const norm = (s) => s.toLowerCase().replace(/[^a-z0-9]/g, '');
// Index by normalized basename; prefer the shortest path on collision.
const byBase = new Map();
for (const p of allSvgs) {
  const key = norm(basename(p, '.svg'));
  if (!byBase.has(key) || p.length < byBase.get(key).length) byBase.set(key, p);
}

mkdirSync(outDir, { recursive: true });
const matched = [];
const unmatched = [];
for (const id of connectorIds) {
  if (NO_BRAND.has(id) && !OVERRIDE[id]) { unmatched.push(`${id} (no-brand)`); continue; }
  let src = OVERRIDE[id] ? join(nodesBase, OVERRIDE[id]) : null;
  if (!src || !existsSync(src)) {
    const cand = byBase.get(norm(id)) || byBase.get(norm(id.replace(/_/g, '')));
    src = cand || null;
  }
  if (src && existsSync(src)) {
    copyFileSync(src, join(outDir, `${id}.svg`));
    matched.push(id);
  } else {
    unmatched.push(id);
  }
}

// Emit a generated id-list so the resolver only points at logos that exist
// (no 404 round-trips / broken-img flashes for connectors without a brand SVG).
const sorted = [...matched].sort();
const generated = `// AUTO-GENERATED by scripts/import-connector-logos.mjs — do not edit by hand.
// Real brand logos sourced from a local n8n checkout into apps/web/public/connectors/.
export const CONNECTOR_LOGO_IDS: ReadonlySet<string> = new Set([
${sorted.map((id) => `  '${id}',`).join('\n')}
]);
`;
import('node:fs').then(({ writeFileSync }) => {
  writeFileSync(join(repo, 'apps/web/src/components/canvas/connectorLogos.generated.ts'), generated);
});

console.log(`MATCHED ${matched.length}/${connectorIds.length}:`);
console.log(matched.join(' '));
console.log(`\nUNMATCHED ${unmatched.length}:`);
console.log(unmatched.join(' '));
